/**
 * TeamFrame — one-time staging Supabase project provisioner.
 *
 * Uses SUPABASE_ACCESS_TOKEN (personal access token) to create the
 * teamframe-staging project via Supabase Management API.
 *
 * Writes credentials to .env.staging (does NOT print them to stdout).
 *
 * Run once:
 *   node scripts/setup-staging-project.mjs
 *
 * After this script completes, run:
 *   npm run db:apply:staging
 */

import { writeFile, readFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import dotenv from "dotenv";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");

dotenv.config({ path: join(repoRoot, ".env.local") });

const ACCESS_TOKEN = process.env.SUPABASE_ACCESS_TOKEN;
if (!ACCESS_TOKEN) {
  console.error("✗ SUPABASE_ACCESS_TOKEN missing from .env.local");
  process.exit(1);
}

const API = "https://api.supabase.com/v1";

async function apiCall(method, path, body) {
  const res = await fetch(`${API}${path}`, {
    method,
    headers: {
      Authorization: `Bearer ${ACCESS_TOKEN}`,
      "Content-Type": "application/json",
    },
    body: body ? JSON.stringify(body) : undefined,
  });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`API ${method} ${path} failed [${res.status}]: ${text}`);
  }
  return res.json();
}

async function main() {
  console.log("• Fetching organisations…");
  const orgs = await apiCall("GET", "/organizations");
  if (!orgs.length) throw new Error("No organisations found for this access token.");
  const org = orgs[0];
  console.log(`  ✓ Using org: ${org.name} (${org.id})`);

  // Check if teamframe-staging already exists.
  console.log("• Checking for existing teamframe-staging project…");
  const projects = await apiCall("GET", "/projects");
  const existing = projects.find((p) => p.name === "teamframe-staging");
  if (existing) {
    console.log(`  ✓ Project already exists: ${existing.id} (${existing.status})`);
    if (existing.status !== "ACTIVE_HEALTHY") {
      console.log(`  ⚠ Status: ${existing.status} — waiting for ACTIVE_HEALTHY…`);
    }
    await writeEnvFile(existing.id);
    return;
  }

  // Generate a secure DB password.
  const dbPassword = generatePassword();

  console.log("• Creating teamframe-staging project…");
  const project = await apiCall("POST", "/projects", {
    organization_id: org.id,
    name: "teamframe-staging",
    db_pass: dbPassword,
    region: "eu-west-2", // Match common default; adjust if needed.
    plan: "free",
  });
  console.log(`  ✓ Project created: ${project.id}`);
  console.log(`  • Waiting for ACTIVE_HEALTHY status…`);

  // Poll until active.
  let attempts = 0;
  let status = project.status;
  while (status !== "ACTIVE_HEALTHY" && attempts < 60) {
    await sleep(5000);
    attempts++;
    const p = await apiCall("GET", `/projects/${project.id}`);
    status = p.status;
    process.stdout.write(`  [${attempts}] status: ${status}\r`);
  }
  console.log();
  if (status !== "ACTIVE_HEALTHY") {
    console.error(`✗ Project did not become ACTIVE_HEALTHY after ${attempts * 5}s. Status: ${status}`);
    process.exit(1);
  }
  console.log("  ✓ Project is ACTIVE_HEALTHY.");

  await writeEnvFile(project.id, dbPassword);
}

async function writeEnvFile(projectId, dbPassword) {
  console.log("• Fetching API keys…");
  const keys = await apiCall("GET", `/projects/${projectId}/api-keys`);
  const anonKey = keys.find((k) => k.name === "anon")?.api_key;
  const serviceKey = keys.find((k) => k.name === "service_role")?.api_key;
  if (!anonKey || !serviceKey) throw new Error("Could not find anon/service_role keys.");

  const project = await apiCall("GET", `/projects/${projectId}`);
  const projectUrl = `https://${projectId}.supabase.co`;

  // Build DB URL — use the connection pooler format.
  const dbUrl = dbPassword
    ? `postgresql://postgres.${projectId}:${encodeURIComponent(dbPassword)}@aws-0-${project.region ?? "eu-west-2"}.pooler.supabase.com:5432/postgres`
    : "(manual: see Supabase dashboard → Project Settings → Database → Connection string)";

  const envContent = [
    `# Generated by scripts/setup-staging-project.mjs on ${new Date().toISOString()}`,
    `# teamframe-staging project: ${projectId}`,
    `# Dashboard: https://supabase.com/dashboard/project/${projectId}`,
    ``,
    `SUPABASE_URL_STAGING=${projectUrl}`,
    `SUPABASE_ANON_KEY_STAGING=${anonKey}`,
    `SUPABASE_SERVICE_ROLE_KEY_STAGING=${serviceKey}`,
    `SUPABASE_DB_URL_STAGING=${dbUrl}`,
  ].join("\n");

  const envPath = join(repoRoot, ".env.staging");
  await writeFile(envPath, envContent, "utf8");

  console.log("  ✓ .env.staging written.");
  console.log(`  Dashboard: https://supabase.com/dashboard/project/${projectId}`);
  console.log("  Next step: npm run db:apply:staging");
}

function generatePassword() {
  // Generate a 32-char alphanumeric password.
  const chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
  let pw = "";
  for (let i = 0; i < 32; i++) {
    // Use Math.random — acceptable for a staging DB password (not a security boundary).
    pw += chars[Math.floor(Math.random() * chars.length)];
  }
  return pw;
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

main().catch((err) => {
  console.error("✗ Staging project setup failed:", err.message);
  process.exitCode = 1;
});
